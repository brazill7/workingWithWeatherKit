import Foundation
import SwiftUI

func primaryColor() -> Color{
    let primary = Color(UIColor(red: 60/255, green: 0/255, blue: 80/255, alpha: 100))
    return primary as Color
}

func secondaryColor() -> Color{
    let secondary = Color(UIColor(red: 190/255, green: 184/255, blue: 235/255, alpha: 100))
    return secondary as Color
}
